package seminar.model;

public class FileTransfer {
	private String src;
	private String des;

	public FileTransfer() {
	}

	public String getSrc() {
		return src;
	}

	public void setSrc(String src) {
		this.src = src;
	}

	public String getDes() {
		return des;
	}

	public void setDes(String des) {
		this.des = des;
	}

}
